package alumno;

public class Alumno {
    public int id;
    public String nombre;
    public String apellido;
    public int edad;
    public float notas;
    
    public Alumno(int id, String nombre, String apellido, int edad, float notas){
        this.id=id;
        this.nombre=nombre;
        this.apellido=apellido;
        this.edad=edad;
        this.notas=notas;
    }
    
}
